SELECT '实验3.2：SQL基本查询' AS step;

SELECT '3.2-1' AS q;
SELECT SNAME, CITY FROM S;

SELECT '3.2-2' AS q;
SELECT PNAME, COLOR, WEIGHT FROM P;

SELECT '3.2-3' AS q;
SELECT DISTINCT JNO FROM SPJ WHERE SNO = 'S1';

SELECT '3.2-4' AS q;
SELECT P.PNAME, SPJ.QTY
FROM P
JOIN SPJ ON P.PNO = SPJ.PNO
WHERE SPJ.JNO = 'J2';

SELECT '3.2-5' AS q;
SELECT DISTINCT SPJ.PNO
FROM SPJ
WHERE SPJ.SNO IN (SELECT SNO FROM S WHERE CITY = '上海');

SELECT '3.2-6' AS q;
SELECT DISTINCT J.JNAME
FROM J
WHERE J.JNO IN (
    SELECT SPJ.JNO
    FROM SPJ
    JOIN S ON SPJ.SNO = S.SNO
    WHERE S.CITY = '上海'
);

SELECT '3.2-7' AS q;
SELECT J.JNO
FROM J
WHERE NOT EXISTS (
    SELECT 1
    FROM SPJ
    JOIN S ON SPJ.SNO = S.SNO
    WHERE SPJ.JNO = J.JNO AND S.CITY = '天津'
);

SELECT '实验3.3：SQL高级查询（嵌套/集合/分组）' AS step;

SELECT '3.3-1' AS q;
SELECT DISTINCT SPJ.SNO
FROM SPJ
WHERE SPJ.JNO = 'J1';

SELECT '3.3-2' AS q;
SELECT DISTINCT SPJ.SNO
FROM SPJ
WHERE SPJ.JNO = 'J1' AND SPJ.PNO = 'P1';

SELECT '3.3-3' AS q;
SELECT DISTINCT SPJ.SNO
FROM SPJ
JOIN P ON SPJ.PNO = P.PNO
WHERE SPJ.JNO = 'J1' AND P.COLOR = '红';

SELECT '3.3-4' AS q;
SELECT J.JNO
FROM J
WHERE NOT EXISTS (
    SELECT 1
    FROM SPJ
    JOIN S ON SPJ.SNO = S.SNO
    JOIN P ON SPJ.PNO = P.PNO
    WHERE SPJ.JNO = J.JNO AND S.CITY = '天津' AND P.COLOR = '红'
);

SELECT '3.3-5' AS q;
SELECT DISTINCT SPJ1.JNO
FROM SPJ SPJ1
WHERE NOT EXISTS (
    SELECT 1
    FROM SPJ SPJ_S1
    WHERE SPJ_S1.SNO = 'S1'
      AND NOT EXISTS (
          SELECT 1
          FROM SPJ SPJ2
          WHERE SPJ2.JNO = SPJ1.JNO AND SPJ2.PNO = SPJ_S1.PNO
      )
);

SELECT '3.3-6' AS q;
SELECT SPJ.JNO, COUNT(DISTINCT SPJ.PNO) AS part_kind_count, SUM(SPJ.QTY) AS total_qty
FROM SPJ
GROUP BY SPJ.JNO
HAVING SUM(SPJ.QTY) >= 100;

