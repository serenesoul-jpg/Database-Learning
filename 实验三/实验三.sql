SELECT '实验3.1：数据库定义（DDL）' AS step;

CREATE TABLE S (
    SNO CHAR(5) PRIMARY KEY,
    SNAME VARCHAR(50) UNIQUE,
    STATUS INT,
    CITY VARCHAR(50)
) DEFAULT CHARSET = utf8mb4;

CREATE TABLE P (
    PNO CHAR(5) PRIMARY KEY,
    PNAME VARCHAR(50),
    COLOR VARCHAR(20),
    WEIGHT INT
) DEFAULT CHARSET = utf8mb4;

CREATE TABLE J (
    JNO CHAR(5) PRIMARY KEY,
    JNAME VARCHAR(50),
    CITY VARCHAR(50)
) DEFAULT CHARSET = utf8mb4;

CREATE TABLE SPJ (
    SNO CHAR(5) NOT NULL,
    PNO CHAR(5) NOT NULL,
    JNO CHAR(5) NOT NULL,
    QTY INT,
    PRIMARY KEY (SNO, PNO, JNO),
    FOREIGN KEY (SNO) REFERENCES S(SNO),
    FOREIGN KEY (PNO) REFERENCES P(PNO),
    FOREIGN KEY (JNO) REFERENCES J(JNO)
) DEFAULT CHARSET = utf8mb4;

